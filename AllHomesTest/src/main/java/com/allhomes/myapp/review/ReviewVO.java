package com.allhomes.myapp.review;

public class ReviewVO {
	private String userid;
	private int pd_no;
	private String rating_price;
	private String rating_delivery;
	private String rating_design;
	private String rating_durability;
	private String img;
	private String content;
	private String writedate;
	private int s_no;
	
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public int getPd_no() {
		return pd_no;
	}
	public void setPd_no(int pd_no) {
		this.pd_no = pd_no;
	}
	public String getRating_price() {
		return rating_price;
	}
	public void setRating_price(String rating_price) {
		this.rating_price = rating_price;
	}
	public String getRating_delivery() {
		return rating_delivery;
	}
	public void setRating_delivery(String rating_delivery) {
		this.rating_delivery = rating_delivery;
	}
	public String getRating_design() {
		return rating_design;
	}
	public void setRating_design(String rating_design) {
		this.rating_design = rating_design;
	}
	public String getRating_durability() {
		return rating_durability;
	}
	public void setRating_durability(String rating_durability) {
		this.rating_durability = rating_durability;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWritedate() {
		return writedate;
	}
	public void setWritedate(String writedate) {
		this.writedate = writedate;
	}
	public int getS_no() {
		return s_no;
	}
	public void setS_no(int s_no) {
		this.s_no = s_no;
	}
}
